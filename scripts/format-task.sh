if [[ "$FIX" == "fix" ]]; then
  echo -e "${BLUE}üîß –ò—Å–ø—Ä–∞–≤–ª–µ–Ω–∏–µ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è...${NC}"
  echo "$FILES" | xargs -r clang-format -i
  echo -e "${GREEN}‚úÖ –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–æ${NC}"
else
  echo "$FILES" | xargs -r clang-format --dry-run --Werror 2>/dev/null || {
    echo -e "${RED}‚ùå –û–±–Ω–∞—Ä—É–∂–µ–Ω—ã –ø—Ä–æ–±–ª–µ–º—ã —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è${NC}"
    echo -e "${YELLOW}üí° –ó–∞–ø—É—Å—Ç–∏—Ç–µ 'make format-fix' –¥–ª—è –∏—Å–ø—Ä–∞–≤–ª–µ–Ω–∏—è${NC}"
    exit 1
  }
  echo -e "${GREEN}‚úÖ –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –ø—Ä–æ–π–¥–µ–Ω–∞${NC}"
fi
