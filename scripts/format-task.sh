NC='\033[0m' # No Color

# –ï—Å–ª–∏ –ø–µ—Ä–µ–¥–∞–Ω –ø—É—Ç—å –∫ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–π –ø–∞–ø–∫–µ, –∏—Å–ø–æ–ª—å–∑—É–µ–º –µ–≥–æ, –∏–Ω–∞—á–µ —Ç–µ–∫—É—â—É—é –ø–∞–ø–∫—É
TARGET_DIR="${1:-.}"
FIX="${2:-check}"

