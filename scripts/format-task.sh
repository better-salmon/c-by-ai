FILES=$(find "$TARGET_DIR" -maxdepth 1 -type f \( -name "*.c" -o -name "*.h" \) ! -name "*.demo.c" 2>/dev/null | sort)

if [[ -z "$FILES" ]]; then
  echo -e "${YELLOW}üí° –§–∞–π–ª—ã C/H –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ $TARGET_DIR${NC}"
  exit 0
